package loop_types;

public class WhileLoop_9Table {

    public static void main(String[] args) {

        System.out.println("-----9 Times Table Using WhileLoop-----");

        int a = 9, i = 1;

        while (i<=10) {

            System.out.println(a+"*"+i+" = "+(a*i));
            i++;

        }
    }
}